int		ft_sqrt(int nb)
{
	int x;

	x = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (x <= nb)
	{
		if (nb == x * x)
		{
			return (x);
		}
		x++;
	}
	return (0);
}

int	ft_count_if(char **tab, int (*f)(char *))
{
	int cntr;
	int i;

	cntr = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]) != 0)
		{
			cntr++;
		}
		i++;
	}
	return (cntr);
}
